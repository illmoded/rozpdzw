import numpy as np
from voice import dzwk,dzwp,dzwj,dzwm
import sklearn.datasets.base
k = dzwk.mfcc #1
p = dzwp.mfcc #2
j = dzwj.mfcc #3
m = dzwm.mfcc #4
#

L,D = np.shape(k[1])
LD = L*D

A = np.empty([0,LD])
name = []
for o in [k,p,j,m]:
    if o == k:
        osoba = 'krzys'
    elif o == p:
        osoba = 'pawel'
    elif o == j:
        osoba = 'justynka'
    elif o == m:
        osoba = 'madzia'
    else:
        print('yay wtf')
    for smffc in o:
        a = np.array(smffc).ravel().reshape(1, -1)
        if np.shape(a)[1] == LD:
            A = np.vstack((A,a))
            name.append(osoba)

dataset = sklearn.datasets.base.Bunch(data = A, target=np.array(name))

